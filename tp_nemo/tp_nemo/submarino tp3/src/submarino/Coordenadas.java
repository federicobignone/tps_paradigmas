package submarino;

public class Coordenadas {

    public int x = 0;
    public int y = 0;

    public Coordenadas(int x, int y) {
        this.x = x;
        this.y = y;

    }

    public int getY() {
        return this.y;
    }

    public int getX() {
        return this.x;
    }
}
